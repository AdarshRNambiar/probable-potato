public class Switch{
    public static void main (String[] args){
        int card=6;
        switch(card){
            case 1:System.out.println("1st card");//can be used for only int and string
                    break;
            case 6:System.out.println("6st card");
                    break;
            default:System.out.println("No card");
        }
    }
}